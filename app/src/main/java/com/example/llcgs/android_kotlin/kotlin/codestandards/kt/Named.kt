/**
 * 命名风格
 *
 *   如果拿不准的时候，默认使⽤Java的编码规范，⽐如：
     使⽤驼峰法命名（并避免命名含有下划线）
     类型名以⼤写字⺟开头
     ⽅法和属性以⼩写字⺟开头
     使⽤ 4 个空格缩进
     公有函数应撰写函数⽂档，这样这些⽂档才会出现在 Kotlin Doc 中
 *
 * */

/**
 * 函数还是属性
 *
 *   很多场合⽆参的函数可与只读属性互换。 尽管语义相近，也有⼀些取舍的⻛格约定。
     底层算法优先使⽤属性⽽不是函数：
     不会抛异常
     O(1) 复杂度
     计算廉价（或缓存第⼀次运⾏）
     不同调⽤返回相同结果
 *
 * */


